package main.java.variables;

public class Variables {
    public static void main(String... args) {
        int n = 5;
        float fn = 5.5f;
        char c = 'C';
        String name = "foo";
        boolean b = true;

        System.out.println(n);
        System.out.println(fn);
        System.out.println(c);
        System.out.println(name);
        System.out.println(b);
    }
}
